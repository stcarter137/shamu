package shamu.media.render;

public class ShadowException extends RuntimeException {

   /**
    * Constructs a shadow exception without a specific message.
    */

   public ShadowException() { super(); }

   /**
    * Constructs a shadow exception with the message specified by the string argument.
    *
    * @param message the detailed message associated with this shadowexception.
    */

   public ShadowException(String message) { super(message); }

}